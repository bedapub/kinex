from importlib import resources
import json
import pandas as pd

def get_pssm() -> pd.DataFrame:
    with resources.path("data", "pssm_table.csv") as df:
        return pd.read_csv(df, index_col=0)
    
def get_groups() -> dict:
    with resources.path("data", "groups.json") as file_path:
        with open(file_path) as json_file:
            return json.load(json_file)
        
def get_experiments() -> dict:
    with resources.path("data", "experiments.json") as file_path:
        with open(file_path) as json_file:
            return json.load(json_file)